import Foundation

import UIKit

extension UIColor {
	public struct STColor {

		// MARK: - Accent Colors
		@nonobjc static var redColor: UIColor {
			return UIColor(named: "redColor")!
		}

		@nonobjc static var orangeColor: UIColor {
			return UIColor(named: "orangeColor")!
		}

	}
}
